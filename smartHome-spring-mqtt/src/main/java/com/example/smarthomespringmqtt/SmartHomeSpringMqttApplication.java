@SpringBootApplication
public class SmartHomeSpringMqttApplication extends SpringBootServletInitializer {

    @Autowired
    Runnable MessageListener;

    @Override
    protected SpringApplicationBuilder configure(SpringApplicationBuilder application) {
        return application.sources(SmartHomeSpringMqttApplication.class);
    }

    public static void main(String[] args) {
        SpringApplication.run(SmartHomeSpringMqttApplication.class, args);
    }

    @Bean
    public CommandLineRunner schedulingRunner(TaskExecutor executor) {
        return new CommandLineRunner() {
            public void run(String... args) throws Exception {
                executor.execute(MessageListener);
            }
        };
    }
}